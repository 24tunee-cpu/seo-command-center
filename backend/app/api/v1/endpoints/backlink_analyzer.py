"""
API Endpoints for Backlink Toxicity Analysis.
Identifies spam/toxic referring domains.
"""
from typing import List, Optional

from fastapi import APIRouter, HTTPException, Query
from pydantic import BaseModel

from app.models.database import BacklinkProfile, Project

router = APIRouter()


# ============== REQUEST/RESPONSE MODELS ==============
class BacklinkCheckRequest(BaseModel):
    project_id: str
    referring_domain: str
    referring_page: str
    target_page: str
    anchor_text: str


class BacklinkResponse(BaseModel):
    id: str
    referring_domain: str
    referring_page: str
    target_page: str
    anchor_text: str
    toxic_score: float
    is_suspicious: bool
    suspicion_reasons: List[str]
    disavow_recommended: bool
    disavowed: bool
    discovered_at: str


# ============== ENDPOINTS ==============
