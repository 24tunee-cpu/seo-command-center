@router.get("/profile/{project_id}", response_model=List[BacklinkResponse])
async def get_backlink_profile(
    project_id: str,
    toxic_only: bool = False,
    limit: int = Query(default=100, le=500)
):
    """
    Get all backlinks for a project.
    
    Optionally filter to show only toxic/suspicious links.
    """
    query = BacklinkProfile.find(BacklinkProfile.project_id == project_id)
    
    if toxic_only:
        query = query.find(BacklinkProfile.toxic_score > 0.5)
    
    backlinks = await query.sort(-BacklinkProfile.metrics.toxic_score).limit(limit).to_list()
    
    return [_backlink_to_response(b) for b in backlinks]


@router.get("/disavow-list/{project_id}")
async def generate_disavow_list(project_id: str):
    """
    Generate a disavow.txt formatted list of toxic domains.
    """
    toxic_links = await BacklinkProfile.find(
        BacklinkProfile.project_id == project_id,
        BacklinkProfile.disavow_recommended == True,
        BacklinkProfile.disavowed == False
    ).to_list()
    
    # Extract unique domains
    domains = set()
    for link in toxic_links:
        domains.add(link.referring_domain)
    
    disavow_text = "# Generated by SEO Command Center\n"
    disavow_text += f"# Total domains: {len(domains)}\n\n"
    disavow_text += "\n".join(f"domain:{d}" for d in sorted(domains))
    
    return {
        "total_domains": len(domains),
        "disavow_format": disavow_text,
        "domains": sorted(domains)
    }


@router.post("/mark-disavowed/{backlink_id}")
async def mark_disavowed(backlink_id: str):
    """
    Mark a backlink as disavowed.
    """
    backlink = await BacklinkProfile.get(backlink_id)
    if not backlink:
        raise HTTPException(status_code=404, detail="Backlink not found")
    
    backlink.disavowed = True
    await backlink.save()
    
    return {"message": "Backlink marked as disavowed"}


def _calculate_toxicity(domain: str, anchor: str) -> tuple:
    """
    Calculate toxicity score based on various spam indicators.
    """
    from datetime import datetime
    
    score = 0.0
    reasons = []
    
    # Check for spam TLDs
    spam_tlds = ['.xyz', '.tk', '.ml', '.cf', '.ga', '.top', '.click', '.link']
    if any(domain.endswith(tld) for tld in spam_tlds):
        score += 0.3
        reasons.append("Spam TLD detected")
    
    # Check for excessive exact match anchors
    if len(anchor) > 30 and anchor.count(' ') < 2:
        score += 0.2
        reasons.append("Potential exact-match anchor")
    
    # Check for suspicious patterns
    if any(x in domain for x in ['porn', 'xxx', 'casino', 'viagra', 'loan']):
        score += 0.5
        reasons.append("Suspicious content indicators")
    
    # Check for numeric-heavy domains
    digit_count = sum(c.isdigit() for c in domain)
    if digit_count > len(domain) * 0.3:
        score += 0.2
        reasons.append("Numeric-heavy domain")
    
    return min(score, 1.0), reasons


def _backlink_to_response(backlink: BacklinkProfile) -> BacklinkResponse:
    return BacklinkResponse(
        id=str(backlink.id),
        referring_domain=backlink.referring_domain,
        referring_page=backlink.referring_page,
        target_page=backlink.target_page,
        anchor_text=backlink.anchor_text,
        toxic_score=backlink.metrics.toxic_score,
        is_suspicious=backlink.is_suspicious,
        suspicion_reasons=backlink.suspicion_reasons,
        disavow_recommended=backlink.disavow_recommended,
        disavowed=backlink.disavowed,
        discovered_at=backlink.discovered_at.isoformat()
    )
